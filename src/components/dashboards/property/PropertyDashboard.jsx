function OwnerStatementPDF({ data }) {
  const { property, owner, month, rentCollected, commission, managementFee, expenses, netPayout, businessName, generatedAt } = data
  return (
    <Document>
      <Page size="A4" style={ps.page}>
        <View style={ps.header}>
          <View>
            <Text style={ps.brand}>{businessName || 'Tlhiso Property'}</Text>
            <Text style={ps.sub}>Owner Statement</Text>
          </View>
          <View style={{ alignItems: 'flex-end' }}>
            <Text style={ps.sub}>Period: {month}</Text>
            <Text style={ps.sub}>Generated: {generatedAt}</Text>
          </View>
        </View>

        <Text style={ps.title}>Statement for {property} — {owner}</Text>

        <View style={ps.row}><Text style={ps.label}>Rent Collected</Text><Text style={ps.value}>{fmt(rentCollected)}</Text></View>
        <View style={ps.row}><Text style={ps.label}>Commission ({commission}%)</Text><Text style={ps.value}>({fmt(rentCollected * commission / 100)})</Text></View>
        <View style={ps.row}><Text style={ps.label}>Management Fee</Text><Text style={ps.value}>({fmt(managementFee)})</Text></View>
        <View style={ps.row}><Text style={ps.label}>Expenses / Maintenance</Text><Text style={ps.value}>({fmt(expenses)})</Text></View>
        <View style={ps.total}>
          <Text style={ps.totalLabel}>Net Payout to Owner</Text>
          <Text style={ps.totalValue}>{fmt(netPayout)}</Text>
        </View>

        <View style={ps.footer}>
          <Text>Generated by Tlhiso — tlhiso.com</Text>
          <Text>Confidential</Text>
        </View>
      </Page>
    </Document>
  )
}

async function downloadStatementPDF(data) {
  const blob = await pdf(<OwnerStatementPDF data={data} />).toBlob()
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `Statement_${data.property}_${data.month}.pdf`
  a.click()
  URL.revokeObjectURL(url)
}

