// ── Rent Roll ─────────────────────────────────────────────────────────────────
function RentRoll() {
  const { user } = useAuth()
  const uid = user?.uid
  const tenants = useCollection(uid ? `users/${uid}/tenants` : null)
  const rentRoll = useCollection(uid ? `users/${uid}/rentRoll` : null)

  const now = new Date()
  const defaultMonth = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}`
  const [month, setMonth] = useState(defaultMonth)
  const [payingId, setPayingId] = useState(null)
  const [payModal, setPayModal] = useState(null)
  const [payDate, setPayDate] = useState(new Date().toISOString().slice(0, 10))
  const [applying, setApplying] = useState(false)

  // Entries for selected month
  const monthEntries = useMemo(() => rentRoll.filter(r => r.month === month), [rentRoll, month])

  // For tenants not yet in this month's roll, derive a row
  const rows = useMemo(() => {
    const activeTenants = tenants.filter(t => t.status === 'Active')
    return activeTenants.map(t => {
      const entry = monthEntries.find(e => e.tenantId === t.id)
      const rentAmt = Number(t.rentAmount ?? 0)
      // Arrears aging
      const today = new Date()
      const monthStart = new Date(month + '-01')
      const diffDays = Math.floor((today - monthStart) / 86400000)
      let aging = ''
      if (entry?.status !== 'Paid') {
        if (diffDays >= 90) aging = '90+ days'
        else if (diffDays >= 60) aging = '60 days'
        else if (diffDays >= 30) aging = '30 days'
        else if (diffDays > 0) aging = `${diffDays} days`
      }
      // Escalation detection
      const escMonth = Number(t.escalationMonth)
      const escRate = Number(t.escalationRate ?? 10)
      const currentMonth = now.getMonth() + 1
      const isEscalationMonth = escMonth && escMonth === currentMonth
      return {
        id: entry?.id,
        tenantId: t.id,
        name: `${t.firstName} ${t.lastName}`,
        property: t.property,
        unit: t.unitNumber,
        rentAmount: rentAmt,
        status: entry?.status ?? 'Pending',
        paidDate: entry?.paidDate ?? '',
        aging,
        isEscalationMonth,
        escRate,
        escalationRate: t.escalationRate,
      }
    })
  }, [tenants, monthEntries, month])

  const paid = rows.filter(r => r.status === 'Paid').length
  const arrears = rows.filter(r => r.status === 'Arrears').length
  const totalExpected = rows.reduce((s, r) => s + r.rentAmount, 0)
  const totalCollected = rows.filter(r => r.status === 'Paid').reduce((s, r) => s + r.rentAmount, 0)

  async function recordPayment(row) {
    if (!uid) return
    setPayingId(row.tenantId)
    if (row.id) {
      await updateDoc(doc(db, 'users', uid, 'rentRoll', row.id), { status: 'Paid', paidDate: payDate })
    } else {
      await addDoc(collection(db, 'users', uid, 'rentRoll'), {
        tenantId: row.tenantId, tenantName: row.name, property: row.property,
        unit: row.unit, rentAmount: row.rentAmount, month, status: 'Paid', paidDate: payDate,
        createdAt: serverTimestamp(),
      })
    }
    setPayingId(null)
    setPayModal(null)
  }

  async function markArrears(row) {
    if (!uid) return
    if (row.id) {
      await updateDoc(doc(db, 'users', uid, 'rentRoll', row.id), { status: 'Arrears' })
    } else {
      await addDoc(collection(db, 'users', uid, 'rentRoll'), {
        tenantId: row.tenantId, tenantName: row.name, property: row.property,
        unit: row.unit, rentAmount: row.rentAmount, month, status: 'Arrears',
        createdAt: serverTimestamp(),
      })
    }
  }

  async function applyEscalation(row) {
    if (!uid || !row.tenantId) return
    setApplying(row.tenantId)
    const newRent = Math.round(row.rentAmount * (1 + row.escRate / 100))
    await updateDoc(doc(db, 'users', uid, 'tenants', row.tenantId), { rentAmount: String(newRent) })
    setApplying(null)
  }

  const agingColor = a => {
    if (a.includes('90')) return 'text-red-600 font-bold'
    if (a.includes('60')) return 'text-orange-500 font-semibold'
    if (a.includes('30')) return 'text-amber-500'
    return 'text-ink-secondary'
  }

  const statusBadge = s => ({
    Paid: 'bg-green-100 text-green-700',
    Arrears: 'bg-red-100 text-red-600',
    Pending: 'bg-gray-100 text-gray-500',
  }[s] ?? 'bg-gray-100 text-gray-500')

  return (
    <div className="space-y-4">
      <PageHead title="Rent Roll" subtitle="Daily view — who's paid, who's late"
        action={
          <div className="flex items-center gap-2">
            <input type="month" value={month} onChange={e => setMonth(e.target.value)}
              className="rounded-xl border border-border px-3 py-2 text-sm outline-none focus:border-primary" />
          </div>
        }
      />

      {/* Summary cards */}
      <div className="grid grid-cols-2 gap-4 lg:grid-cols-4">
        <StatCard label="Expected" value={fmt(totalExpected)} icon="📋" trendTone="flat" />
        <StatCard label="Collected" value={fmt(totalCollected)} icon="✅" color="blue" trendTone="up" />
        <StatCard label="Outstanding" value={fmt(totalExpected - totalCollected)} icon="⏳" color="orange" trendTone={totalCollected < totalExpected ? 'down' : 'up'} />
        <StatCard label="In Arrears" value={arrears} icon="🚨" color="orange" trend={`${paid} paid`} trendTone={arrears ? 'down' : 'up'} />
      </div>

      {/* Rent roll table */}
      <div className="overflow-hidden rounded-card border border-border bg-white shadow-card">
        <div className="overflow-x-auto">
          <table className="min-w-full text-sm">
            <thead className="bg-surface-2 text-xs font-semibold text-ink-secondary">
              <tr>
                {['Tenant', 'Property', 'Unit', 'Rent', 'Status', 'Arrears Age', 'Actions'].map(h => (
                  <th key={h} className="px-4 py-3 text-left">{h}</th>
                ))}
              </tr>
            </thead>
            <tbody className="divide-y divide-border">
              {rows.length === 0 && (
                <tr><td colSpan={7} className="px-4 py-8 text-center text-ink-secondary text-sm">No active tenants for this month.</td></tr>
              )}
              {rows.map(row => (
                <tr key={row.tenantId} className="hover:bg-surface-2/50 transition">
                  <td className="px-4 py-3 font-medium text-ink">
                    {row.name}
                    {row.isEscalationMonth && (
                      <span className="ml-2 inline-flex items-center gap-1 rounded-full bg-amber-100 px-2 py-0.5 text-[10px] font-semibold text-amber-700">
                        <TrendingUp size={10} /> Escalation Due
                      </span>
                    )}
                  </td>
                  <td className="px-4 py-3 text-ink-secondary">{row.property}</td>
                  <td className="px-4 py-3 text-ink-secondary">{row.unit}</td>
                  <td className="px-4 py-3 font-semibold text-ink">{fmt(row.rentAmount)}</td>
                  <td className="px-4 py-3">
                    <span className={`rounded-full px-2 py-0.5 text-xs font-semibold ${statusBadge(row.status)}`}>{row.status}</span>
                  </td>
                  <td className={`px-4 py-3 text-xs ${row.aging ? agingColor(row.aging) : 'text-ink-secondary'}`}>
                    {row.aging || (row.status === 'Paid' ? '—' : 'Current')}
                  </td>
                  <td className="px-4 py-3">
                    <div className="flex items-center gap-1 flex-wrap">
                      {row.status !== 'Paid' && (
                        <>
                          <button onClick={() => { setPayModal(row); setPayDate(new Date().toISOString().slice(0,10)) }}
                            className="rounded px-2 py-1 text-xs font-semibold text-green-700 bg-green-50 hover:bg-green-100">
                            {payingId === row.tenantId ? <Loader2 size={12} className="animate-spin" /> : 'Mark Paid'}
                          </button>
                          <button onClick={() => markArrears(row)}
                            className="rounded px-2 py-1 text-xs font-semibold text-red-600 bg-red-50 hover:bg-red-100">
                            Arrears
                          </button>
                        </>
                      )}
                      {row.isEscalationMonth && row.status === 'Paid' && (
                        <button onClick={() => applyEscalation(row)}
                          className="rounded px-2 py-1 text-xs font-semibold text-amber-700 bg-amber-50 hover:bg-amber-100">
                          {applying === row.tenantId ? <Loader2 size={12} className="animate-spin" /> : `Apply ${row.escRate}% Escalation`}
                        </button>
                      )}
                    </div>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      </div>

      {/* Pay modal */}
      <Modal open={!!payModal} onClose={() => setPayModal(null)} title="Record Payment">
        {payModal && (
          <div className="space-y-4">
            <p className="text-sm text-ink">Recording payment for <strong>{payModal.name}</strong> — {fmt(payModal.rentAmount)}</p>
            <Field label="Payment Date" type="date" value={payDate} onChange={e => setPayDate(e.target.value)} />
            <button onClick={() => recordPayment(payModal)} className="w-full rounded-xl bg-primary py-2.5 text-sm font-semibold text-white">Confirm Payment</button>
          </div>
        )}
      </Modal>
    </div>
  )
}


